/**
 * Fungsi helper untuk mendapatkan karyawan berdasarkan ID
 */
export function getEmployeeById(employeeId: string): MasterEmployee | undefined {
  return MASTER_EMPLOYEES.find(emp => emp.employeeId === employeeId);
}

/**
 * Fungsi helper untuk mendapatkan karyawan berdasarkan departemen
 */
export function getEmployeesByDepartment(department: string): MasterEmployee[] {
  return MASTER_EMPLOYEES.filter(emp => emp.department === department);
}

/**
 * Fungsi helper untuk mendapatkan karyawan berdasarkan divisi
 */
export function getEmployeesByDivision(division: string): MasterEmployee[] {
  return MASTER_EMPLOYEES.filter(emp => emp.division === division);
}

/**
 * Fungsi helper untuk mendapatkan karyawan berdasarkan status
 */
export function getEmployeesByStatus(status: 'active' | 'inactive' | 'on-leave'): MasterEmployee[] {
  return MASTER_EMPLOYEES.filter(emp => emp.status === status);
}
